function add(a, b) {
  return a+b;
}

function subtract(a, b) {
  return a - b;
}

function sum(a, b) {
  return a + b;
}

function multiply(a, b) {
  return a * b;
}

function power(a) {
  return a ** 2;
}

function factorial(a, b, c, d) {
  return a * b * c * d;
}

module.exports = {
  add,
  subtract,
  sum,
  multiply,
  power,
  factorial,
};

